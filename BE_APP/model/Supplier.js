class Supplier {
    constructor(id, name, phone, email, location, url, background_url, description) {
        this.id = id;
        this.name = name;
        this.phone = phone;
        this.email = email;
        this.location = location
        this.url = url
        this.background_url = background_url,
        this.description = description
    }
}

module.exports = Supplier;